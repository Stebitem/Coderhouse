a = 10
b = -5
c = "Hola"
d = [1, 2, 3]
e = (4, 5, 6)

print(a * 5)           # 50
print(a - b)           # 15
print(c + "Mundo")     # HolaMundo
print(c * 2)           # HolaHola
print(c[-1])           # a
print(c[1:])           # ola
print(d + d)           # [1, 2, 3, 1, 2, 3]
print(e[1])            # 5
print(e + (7, 8, 9))   # (4, 5, 6, 7, 8, 9)